// switch statements
const grade = "D";

// without the breaks it would carry going through each case
switch (grade) {
 case "A":
  console.log("Congrats you got an A!");
  break;
 case "B":
  console.log("you got a B!");
  break;
 case "C":
  console.log("you got a C!");
  break;
 case "D":
  console.log("you got a D!");
  break;
 case "E":
  console.log("Sorry you got an E!");
  break;
 default:
  console.log("not a valid grade");
}

